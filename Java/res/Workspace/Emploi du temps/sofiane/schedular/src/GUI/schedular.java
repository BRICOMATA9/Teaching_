    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        Salle s = new Salle(cbType.getSelectedItem().toString(), tfLibelle.getText(),
                SalleDAO.getInstance().findSalleByLibelle(cbSuccesseur.getSelectedItem().toString()),
                SalleDAO.getInstance().findSalleByLibelle(cbPredecesseur.getSelectedItem().toString()),
                parseInt(sCapacite.getValue().toString()));
        SalleDAO.getInstance().insertSalle(s);
        if (SalleDAO.getInstance().insertSalle(s)) {
            JOptionPane.showMessageDialog(this, "Ajout effectué avec succès");
            initJtableSalle(SalleDAO.getInstance().displayAllSalle());
            FillComboBox(cbPredecesseur, SalleDAO.getInstance().displayAllSalle());
            FillComboBox(cbSuccesseur,SalleDAO.getInstance().displayAllSalle());
            cbType.setSelectedIndex(0);
            cbPredecesseur.setSelectedIndex(0);
            cbSuccesseur.setSelectedIndex(0);
            sCapacite.setValue(20);
            tfLibelle.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "l'Ajout a échoué");
        }

    }//GEN-LAST:event_btnAjouterActionPerformed

