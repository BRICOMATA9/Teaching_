    void initRecherche(String query, JTable jt) {
        DefaultTableModel dtm = (DefaultTableModel) jt.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(dtm);
        jt.setRowSorter(tr);
        tr.setRowFilter(javax.swing.RowFilter.regexFilter(query));
    }

    public void FillComboBox(JComboBox cb, List list) {
        List<String>lista=new ArrayList<>();
        list.stream().forEach((object)->lista.add(object.toString()));
        Collections.sort(lista,(String a,String b)->{return a.compareTo(b);});
        cb.removeAllItems();
        cb.addItem("");
        lista.stream().forEach((object) -> {
            cb.addItem(object);
        });
    }
